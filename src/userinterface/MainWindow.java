    private void addChiliesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addChiliesButtonActionPerformed
        selectedChili = chiliesSelectionComboBox.getSelectedIndex();
        Integer amountOfChiliesToBuy = (Integer) amountOfChiliesToAdd.getValue();

        cartManager.addChiliesToCart(cart, amountOfChiliesToBuy, selectedChili);

        nameOfChili = chiliesSelectionComboBox.getSelectedItem().toString();

        if (nameOfChili.contains("Please")) {
            feedback.setText("*** Please choose a chili ***");
        } else if (amountOfChiliesToBuy < 1) {
            feedback.setText("*** Please enter a positive number ***");
        } else if (!nameOfChili.contains("Please")) {
            feedback.setText(amountOfChiliesToBuy + " " + nameOfChili + " added.");
        }

        amountOfChiliesToAdd.setValue(0);
    }//GEN-LAST:event_addChiliesButtonActionPerformed

