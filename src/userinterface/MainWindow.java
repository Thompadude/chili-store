    private void removeChiliesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeChiliesButtonActionPerformed
        Integer amountOfChiliesToRemove = (Integer) amountOfChiliesToAdd.getValue();
        nameOfChili = chiliesSelectionComboBox.getSelectedItem().toString();

        String feedbackText = "*** Please enter a positive number ***";

        if (!(amountOfChiliesToRemove < 1)) {
            feedbackText = cartManager.removeSelectedChiliFromCart(cart, nameOfChili, amountOfChiliesToRemove);
        }

        if (amountOfChiliesToRemove < 1) {
            feedback.setText(feedbackText);
        }

        amountOfChiliesToAdd.setValue(0);

        if (!nameOfChili.contains("Please")) {
            feedback.setText(feedbackText);
        } else {
            feedback.setText("*** Please choose a chili ***");
        }
    }//GEN-LAST:event_removeChiliesButtonActionPerformed

